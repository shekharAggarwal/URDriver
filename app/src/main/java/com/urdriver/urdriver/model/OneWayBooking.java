package com.urdriver.urdriver.model;

public class OneWayBooking {
    String id, cabStatus, cabBrand, cabModel, cabSitting, cabImage, cabPrice, cabFare;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getCabStatus() {
        return cabStatus;
    }

    public void setCabStatus(String cabStatus) {
        this.cabStatus = cabStatus;
    }

    public String getCabBrand() {
        return cabBrand;
    }

    public void setCabBrand(String cabBrand) {
        this.cabBrand = cabBrand;
    }

    public String getCabModel() {
        return cabModel;
    }

    public void setCabModel(String cabModel) {
        this.cabModel = cabModel;
    }

    public String getCabSitting() {
        return cabSitting;
    }

    public void setCabSitting(String cabSitting) {
        this.cabSitting = cabSitting;
    }

    public String getCabImage() {
        return cabImage;
    }

    public void setCabImage(String cabImage) {
        this.cabImage = cabImage;
    }

    public String getCabPrice() {
        return cabPrice;
    }

    public void setCabPrice(String cabPrice) {
        this.cabPrice = cabPrice;
    }

    public String getCabFare() {
        return cabFare;
    }

    public void setCabFare(String cabFare) {
        this.cabFare = cabFare;
    }
}
